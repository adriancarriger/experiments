import reverseVowels from './reverse-vowels-of-a-string';

describe('...', () => {
  it('...', () => {
    expect(reverseVowels('hello')).toBe('holle');
  });

  it('...', () => {
    expect(reverseVowels('abcdef')).toBe('ebcdaf');
  });

  it('...', () => {
    expect(reverseVowels('leetcode')).toBe('leotcede');
  });
});
