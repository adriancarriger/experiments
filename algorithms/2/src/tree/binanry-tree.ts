export class BinaryTreeNode {
  public left: BinaryTreeNode;
  public right: BinaryTreeNode;

  constructor(public value) {
    this.left = null;
    this.right = null;
  }

  insertLeft(value) {
    this.left = new BinaryTreeNode(value);
    return this.left;
  }

  insertRight(value) {
    this.right = new BinaryTreeNode(value);
    return this.right;
  }
}
